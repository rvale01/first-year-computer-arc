void init()
{
	int i;

	for (i = 0; i < NUMBER_OF_COUNTERS; i++)
	{
		counters[i] = 0;
	}
}



void tabulate()
{
	int i;

	for (i = 0; i < NUMBER_OF_CARDS; i++)
	{
		counters[0] = counters[0] + cards[i][AGE_20s];
		counters[1] = counters[1] + cards[i][AGE_30s];
		counters[2] = counters[2] + cards[i][AGE_40s];
		counters[3] = counters[3] + cards[i][AGE_50s];
		counters[4] = counters[4] + cards[i][AGE_60s];
		counters[5] = counters[5] + cards[i][AGE_70s];
		counters[6] = counters[6] + cards[i][MALE];
		counters[7] = counters[7] + cards[i][FEMALE];
		counters[8] = counters[8] + cards[i][POLYURIA];
		counters[9] = counters[9] + cards[i][POLYDIPSIA];
		counters[10] = counters[10] + cards[i][SUDDEN_WEIGHT_LOSS];
		counters[11] = counters[11] + cards[i][WEAKNESS];
		counters[12] = counters[12] + cards[i][POLYPHAGIA];
		counters[13] = counters[13] + cards[i][GENITAL_THRUSH];
		counters[14] = counters[14] + cards[i][VISUAL_BLURRING];
		counters[15] = counters[15] + cards[i][ITCHING];
		counters[16] = counters[16] + cards[i][IRRITABILITY];
		counters[17] = counters[17] + cards[i][DELAYED_HEALING];
		counters[18] = counters[18] + cards[i][PARTIAL_PARESIS];
		counters[19] = counters[19] + cards[i][MUSCLE_STIFFNESS];
		counters[20] = counters[20] + cards[i][ALOPECIA];
		counters[21] = counters[21] + cards[i][OBESITY];
		counters[22] = counters[22] + cards[i][DIABETIC];
		counters[23] = counters[23] + cards[i][NOTDIABETIC];

		counters[24] = counters[24] + (cards[i][MALE] | cards[i][FEMALE]);			/* Male or female, i.e. the number of cards */
		counters[25] = counters[25] + (cards[i][DIABETIC]);							/* Number people with diabetese */
		counters[26] = counters[26] + (cards[i][NOTDIABETIC]);						/* Number people without diabetese */

		counters[27] = counters[27] + (cards[i][DIABETIC] & cards[i][POLYDIPSIA]);		/* Number people with polydipsia and with diabetese */
		counters[28] = counters[28] + (cards[i][NOTDIABETIC] & cards[i][POLYDIPSIA]);	/* Number people with polydipsia and without diabetese */
		counters[29] = counters[29] + (cards[i][DIABETIC] & !cards[i][POLYDIPSIA]);		/* Number people without polydipsia and with diabetese */
		counters[30] = counters[30] + (cards[i][NOTDIABETIC] & !cards[i][POLYDIPSIA]);	/* Number people without polydipsia and without diabetese */
		
		counters[31] = counters[31] + (cards[i][DIABETIC]& cards[i][ALOPECIA]);
		counters[32] = counters[32] + (cards[i][NOTDIABETIC] & cards[i][ALOPECIA]);
		counters[33] = counters[33] + (cards[i][DIABETIC] & !cards[i][ALOPECIA]);
		counters[34] = counters[34] + (cards[i][NOTDIABETIC] & !cards[i][ALOPECIA]); 

	}
}


void printCounters()
{
	int i;

	for (i = 0; i < NUMBER_OF_COUNTERS; i++)
	{
		printf("counters[%2d] = %d\n", i, counters[i]);
	}
}


int main()
{
	init();

    printf("Hollerith Tabulating Machine\n\n");

	tabulate();
	printCounters();
}


